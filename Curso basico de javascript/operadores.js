/* Existen los siguientes tipos de operadores */

/* 1. Los operadores aritmeticos, que son los de toda la vida */

9 + 0
10 * 9
9 - 8 
20 / 2 

"Yefry" + "Rodelo" /* Tambien este operador puede concatenar palabras */

/* 2. operadores unitario: se llaman asi ya que solo operan con 1 solo valor */

!false

/* 3. operadores de asignacion: Como dice el nombre asigna valores, y es el operador "=" */

var a = 1;

/* 4. Operadores de comparacion: Como indica el nombre este se encarga de comparar valores y existen 2 operadores que son "==", "===", "<", ">", "<=" y ">=" */

3 == "3"; /* Este solo compara el valor, no el tipo de dato */

3 === "3"; /* Este compara tanto el valor como el tipo de dato */

/* 5. operadores de validacion: Estos validan la informacion para saber si se ejecuta o no una linea de codigo */

a && b  /* Aqui ambas deben ser verdaderas para que la condicion se cumpla */

a || b /* Aqui si una sola es verdadera se ejecuta la condicion */ 

